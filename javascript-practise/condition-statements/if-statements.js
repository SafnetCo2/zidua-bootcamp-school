
// if/else statements
// An if/else statement is used to execute a block of code if
// a certain condition is true, or a different block of code if the condition is false.


var count =3;
if(count ==4){
    console.log("count is 4")
}else if(count >4){
    console.log('greater than 4')

}
else if(count < 4){
    console.log('less than 4')
}else{
    console.log('not equal to 4')
}

